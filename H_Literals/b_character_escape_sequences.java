package H_Literals;

public class b_character_escape_sequences {
    public static void main(String[] args) {
        // System.out.println("Hello friends !! "Good Morning.""); // error
        System.out.println("Hello friends !! \"Good Morning.\"");
        System.out.println("Hello friends !! \tGood Morning.");
        System.out.println("Hello friends !! \nGood Morning.");
        System.out.println("Hello friends !! \bGood Morning.");
        System.out.println("Hello friends !! \rGood Morning.");
        System.out.println("This is \\ (backslash) used in character escape operation.");
    }
}
